package org.wso2.carbon.tools.securevault.model;

/**
 * This class encapsulate the keystore from the configuration.
 *
 * @since 5.1.0
 */
public class CarbonKeyStore {
    private String location;
    private String type;
    private String password;
    private String keyAlias;

    public CarbonKeyStore(String location, String type, String password, String keyAlias) {
        this.location = location;
        this.type = type;
        this.password = password;
        this.keyAlias = keyAlias;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getKeyAlias() {
        return keyAlias;
    }

    public void setKeyAlias(String keyAlias) {
        this.keyAlias = keyAlias;
    }
}
